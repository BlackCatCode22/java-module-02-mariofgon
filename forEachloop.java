public class forEachloop {

    public static void main(String[] args) {

        String[] cars = {"GTR R35", "Durango SRT", "TRX"};

        for (String car : cars) {
            System.out.println(car);
        }

        for(int i=0; i<5; i++) {

            System.out.println(i);
        }


    }
}
